def get_prompts(channel: str) -> list[str]:
    """Get all prompts for a channel."""
    return PROMPTS.get(channel, [])


def get_prompt(channel: str, index: int) -> str | None:
    """Get a specific prompt by index."""
    prompts = get_prompts(channel)
    if index < len(prompts):
        return prompts[index]
    return None


def list_channels() -> list[str]:
    return list(PROMPTS.keys())
